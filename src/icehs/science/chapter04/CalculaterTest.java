package icehs.science.chapter04;

public class CalculaterTest {
	public static void main(String[] args) {
		
		int number1 = 20;
		int number2 = 7;
		
		int plusResult = number1 + number2;
		int minusResult = number1 - number2;
		int multiResult = number1 * number2;
		int divisionResult = number1 / number2;
		int remainderResult = number1 % number2;
		
		System.out.println("오 써쎀 : " + plusResult);
		System.out.println("짋 써쎀 : " + minusResult);
		System.out.println("썼 써쎀 : " + multiResult);
		System.out.println("씱얋 써쎀 : " + divisionResult);
		System.out.println("씱쟵쵔 써쎀 : " + remainderResult);
	}

}
