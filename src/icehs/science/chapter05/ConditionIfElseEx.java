package icehs.science.chapter05;

public class ConditionIfElseEx {
	public static void main(String[] args) {
		
		int age = 15;
		
		if (age >= 19)
		{
			System.out.println("성인입니다.");
			System.out.println("입장하세요.");
		}
		else
		{
			System.out.println("성인이 아닙니다.");
			System.out.println("집으로 가세요.");
		}
	}

}
